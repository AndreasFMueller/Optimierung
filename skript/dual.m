A = [
  -2, -4, -5,  1,  1,  1,  1,  0,  0,  0,  0,  0;
  -1,  1,  4, -1,  0,  0,  0,  1,  0,  0,  0,  2;
   3,  3,  1,  0, -1,  0,  0,  0,  1,  0,  0,  3;
  -1,  1,  4, -1,  0, -1,  0,  0,  0,  1,  0,  2;
   3,  3,  1,  0, -1,  0, -1,  0,  0,  0,  1,  3
]

function B = pivot(A, i, j)
	B = A;
	B(i,:) = B(i,:) / B(i,j);
	for k = 1:size(B)(1)
		if (k != i)
			B(k,:) = B(k,:) - B(k,j) * B(i,:);
		endif
	endfor
endfunction;

A1 = pivot(A, 2,3)
4 * A1

A2 = pivot(A1, 3,1)
